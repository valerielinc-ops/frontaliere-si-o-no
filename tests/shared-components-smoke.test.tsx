/**
 * shared-components-smoke.test.tsx
 *
 * Smoke tests for shared interactive components.
 * Prevents regressions like commit 36da947c8c9 where a missing
 * useCallback (downloadImage) caused a ReferenceError crash that
 * brought down the entire site via ErrorBoundary.
 *
 * These tests render components with interactive elements and
 * simulate user interactions to catch:
 *  - ReferenceError (undefined function/variable)
 *  - TypeError (calling non-function)
 *  - Missing hook dependencies
 *  - Runtime crashes in event handlers
 *
 * The pre-push hook runs all tests, so these act as a gate
 * preventing broken interactive components from being deployed.
 */

import { describe, it, expect, vi, beforeEach, afterEach } from 'vitest';
import { render, screen, fireEvent, act, cleanup } from '@testing-library/react';
import React from 'react';

